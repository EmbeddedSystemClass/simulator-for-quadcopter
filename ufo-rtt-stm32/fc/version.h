#ifndef _VERSION_H
#define _VERSION_H

#define VER_MAJOR       0x00
#define VER_MINOR       0x01
#define VER_PATCHLEVEL  0x0001

#define VERSION_CUR     ((VER_MAJOR << 12) | (VER_MINOR << 8) | (VER_PATCHLEVEL))


#endif /* _VERSION_H */
